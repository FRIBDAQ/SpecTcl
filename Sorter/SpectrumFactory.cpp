//
//  Function:   
//    CSpectrum* CreateM1D ( const std::string& rName, DataType_t eType, 
//                           CParameter rParameter, Float_t nLow,
//                           Float_t nHigh, UInt_t nChans );
//  Operation Type:
//     Creational.
//
CSpectrum* 
CSpectrumFactory::CreateM1D(const std::string& rName, DataType_t eType, 
			    CParameter rParameter,
			    Float_t nLow, Float_t nHigh, UInt_t nChans) 
{
// Called to create a mapped 1-d spectra.
//
// Formal Parameters:
//     const std::string& rName:
//          Name of the spectrum.
//     DataType_t eType:
//          Type of data contained by spectrum.
//      CParameter rParameter:
//           Parameter on which the spectrum is to be set.
//      Float_t nLow:
//           Low value of the coordinate mapping
//      Float_t nHigh:
//           High value of the coordinate mapping
//      UInt_t nChans:
//           Number of channels to histogram
// Returns:
//    A pointer to the created spectrum.
// Throws:
//    On error throws a CSpectrumFactoryException.
//

  switch(eType) {
  case keWord:
    return new CMSpectrum1DW(rName, NextId(), rParameter,
			     nLow, nHigh, nChans);
  case keLong:
    return new CMSpectrum1DL(rName, NextId(), rParameter,
			     nLow, nHigh, nChans);
  default:			// Only word and long spectra are supported.
    throw CSpectrumFactoryException(eType, keM1D, rName,
				    CSpectrumFactoryException::keBadDataType,
				    "Creating mapped 1d spectrum");
  }
}
