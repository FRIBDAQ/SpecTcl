static inline double frandom()	/* Return random # in range [0,1] */
{
  double val;
  int    r;
  r = rand();
  if(r < 0) r = -r;
  val = (double)r;
  val = val / (float)RANDOM_RANGE;
  return val;
}
static int Xamine_PutPixel8(XImage *i, int x, int y, unsigned long pixel)
{
  i->data[x + y * ((short)i->width)] = (char)pixel;
  return 0;
}



