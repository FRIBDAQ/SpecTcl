{
    FLBL1   label;

    /* First step is to format the label. */

    encstring((char*)&label, " ", sizeof(FLBL1));	/* Blank fill the label. */
    encstring(label.hdrid, hdrname, IDSIZE);	    /* Encode label name. */
    encstring(label.filename, vol->filename, FILEIDENT); /* file id. */
    encstring(label.fsetid, vol->volname, FSIDSIZE);	/* File set id. */
    encnum(label.secnum, 1, NUMSIZE);	    /* File section number. */
    encnum(label.seqnum, seqnum, NUMSIZE);  /* File sequence number. */
    encnum(label.generation, 1, NUMSIZE);
    encnum(label.version, 0, VERSIONSIZE);
    encdate(label.credate, &(vol->creation));	/* Creation date. */
    encstring(label.expdate, " ", DATESIZE);	/* No expiration date. */
    label.access = ' ';
    encnum(label.blkcnt, vol->blockswritten,	/* Block count. */
	    BLKCNTSIZ); 
    encstring(label.implementor,		/* Implementation name. */
	      STDIMP, IMPSIZE);
     
    /* At this time, the header label is all formated so we can		    */
    /* procede to attempt to write it to the tape. */
     
    return mtwrite(vol->fd, (void*)&label, sizeof(FLBL1));
}


/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      lbl2write   - This function writes any of the following file label
**		    records:
**			HDR2, EOF2, EOV2.
**			All of these have the same format.
**
**  FORMAL PARAMETERS:
**
**      volume	*vcb	- Pointer to volume control block.  The following
**			  fields make up fields in the label:
**			    Block Length = reclen+offlen
**			    Record Length= reclen
**			    Offset Length= offlen
**			  Record format will always be fixed.
**	char	*lblname- Name of label to write.
**
**  FUNCTION VALUE:
**
**      Status of the mtwrite() call.
**
**
**--
*/
static int lbl2write (volume* vol, char* lblname)
{
    FLBL2   label;

	/* First it's necessary to fill in the header. */

    encstring((char*)&label, " ", sizeof(FLBL2));	/* First blankf fill. */
    encstring(label.hdrid, lblname, IDSIZE);	/* Label name. */
    label.recordfmt = 'F';			/* Only support fixed records. */
    encnum(label.blocklen, vol->reclen + vol->offlen, BLKSIZE);
    encnum(label.recordlen, vol->reclen, BLKSIZE);
    encnum(label.offlen, vol->offlen, OFFSIZE);
     
	/* Now we write the formatted lable. */
     
    return mtwrite(vol->fd, (void*)&label, sizeof(FLBL2));
}


/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      writetrailers	- This function writes all of the stuff required to
**			  close a file.  This includes a tape mark, followed
**			  by EOF1, and EOF2 and two more tape marks.  The
**			  tape is left positioned between the last two 
**			  consecutive tape marks.
**
**  FORMAL PARAMETERS:
**
**      volume	*vcb	- Pointer to the volume control block.
**
**
**  FUNCTION VALUE:
**
**      On a failing I/O we immediately return the I/O status, this could
**	leave a tape improperly closed, but it's not clear there's a good
**	alternative... If you can't write trailers, you can't write trailers.
**	The MTLEOT signal is stored and returned, but we don't exit on it.
**
**
**--
*/
static int writetrailers (volume* vcb)
{
    int status, leotencountered;

    leotencountered = 0;
     
    status = mtweof(vcb->fd, 1);	/* Tape mark before EOF1. */
    if(status == MTEOMED)
    {
	leotencountered = 1;			/* Remember leot. */
    }
    else if (status != MTSUCCESS )
    {
	return status;				/* Return on other failures. */
    }
     
    status = lbl1write(vcb, "EOF1",		/* Write EOF1. */
			vcb->filenumber);
    if (status == MTEOMED)    {
	leotencountered = 1;			/* Remember leot. */
    }
    else if (status != MTSUCCESS)
    {
	return status;				/* Return on other failures. */
    }
    status = lbl2write(vcb, "EOF2");		/* EOF2 record. */
    if (status == MTEOMED)
    {
	leotencountered = 1;			/* Remember leot. */
    }
    else if (status != MTSUCCESS)
    {
	return status;				/* Return on other failures. */
    }
    status = mtweof(vcb->fd, 2);	/* 2 Tape mark trailers. */
    if (status == MTEOMED)
    {
	leotencountered = 1;			/* Remember leot. */
    }
    else if (status != MTSUCCESS)
    {
	return status;				/* Return on other failures. */
    }

	/* All the writes have been performed, now position. */

    status = mtspacef(vcb->fd,  -1);
    if (leotencountered && (status != MTSUCCESS))
    {
	return MTLEOT;
    }
    else if(status != MTSUCCESS)
	return errno;
    else
    {
	return MTSUCCESS;
    }
}


/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      getitem	- This function searches for a particular itme in an itemlist.
**		    since item lists are only used on opens and mounts, there
**		    is no need to be terribly quick so a linear search is done.
**
**  FORMAL PARAMETERS:
** 
**	int		code	- Item code to search for.
**	volume_item	*list	- List to search.
**	char		**item	- Value in item list block.
**	int		term	- Terminator code, tells when at end of list.
**
**
**  FUNCTION VALUE:
**
**      0   - Not found
**	1   - Found.
**
**
**--
*/
static int getitem(int code, volume_item* list, char** item, int term)
{

    while (list->code != term)
    {
	if (list->code == code)
	{
	    *item = list->data;
	    return 1;
	}
	else
	{
	    list++;
	}
    }
    *item = (char *)0;
    return 0;
}


