/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      seekleov    - Seek the logical end of a tape volume.  The volume is
**		    assumed to be valid ANSI.  Thus our task is to hunt for
**		    an EOF1 record closely followed by  double tape marks.
**		    We also need to deal with the special case that we
**		    are at the beginning of the volume (just mounted).
**		    After successful seeks, the tape is positioned either
**		    of two locations:
**		    1. After the VOL1 header.  NO attempt is made to preserve
**			any additional labels which might be on the tape.
**			This is to deal with ANSI 1978 formatted tapes which
**			could have anything at all on the tape after the
**			volume header.
**		    2. After the first tape mark following the last set of 
**		       EOF labels on the tape.  Thus new headers may be
**		       immediately written to create a new file.
**
**  FORMAL PARAMETERS:
**
**      volume	*vcb	- Pointer to the volume control block associated with
**			  the tape.
**
**
**--
*/
static void seekleov (volume *vcb)
{
    int status, 
	readsize;
    FLBL1 label;

    /* There are some conditions where we should not actually move the */
    /* tape around.  These are when we're already at the LEOV, and    */
    /* When we're past the safe part of the tape.  The safe part of the	    */
    /* tape is defined as anywhere after the logical end of tape.    */
    /* Thus for safety we prevent new files from being created after the   */
    /* LEOT is passed. */
     
    if ( (vcb->volloc == LEOV) ||
	 (vcb->volloc == PEOT) ||
	 (vcb->volloc == LEOT) )
    {
	return;
    }
    /* If we are at the beginning of tape then we want to insert before	    */
    /* the first EOF mark after the volume header sequence.  Since we    */
    /* Cannot anticipate all permutations on pre-labelled tapes, we	    */
    /* leave only the minimal volume header on the tape (VOL1). */
     
    if (vcb->volloc == BOT)
    {
	status = mtspacef(vcb->fd,  1);
	if (status != MTSUCCESS)
	{
	    panic("Fatal Magtape I/O error skipping filemarks in seekeov()\n");
	}
	status = mtread(vcb->fd,  
			(char*)&label, sizeof(FLBL1), (unsigned int*)&readsize);
	if (status == MTEOF)		/* If truly BOT should be an EOF. */
	{
	    mtrewind(vcb->fd);  /* Rewind the tape. */
	    mtspacer(vcb->fd,  1); /* Skip VOL1. */
	    vcb->volloc = LEOV;		    /* This is EOV position. */
	    vcb->filenumber = 0;	    /* Before 1'st file. */
	    return;			    /* Positioned. */
	}
    }
    /* If control passes here, we are in the middle of the volume somewhere */
    /* We locate the end of volume making no assumptions about where we are.  */
    /* This is done by looking for an EOF1 label record with 2 tapemarks   */
    /* soon after it.  Soon is defined as the next 2 tapemarks being 	    */
    /* consecutive. */

    while (1)
    {
	status = mtread(vcb->fd,
			(char*)&label, sizeof(FLBL1), (unsigned int*)&readsize);
	if (status == MTIO)
	{
	    panic("Unclosed tape found in drive in seekleov()\n");
	}
	mtspacef(vcb->fd, 1);	/* Skip an EOF. */
	if ((status == MTSUCCESS)		 && 
	    (readsize == sizeof(FLBL1))	 &&
	    (strncmp (label.hdrid, "EOF1", IDSIZE) == 0))
	{				    /* EOF label encountered. */

	    status = mtread(vcb->fd,
			  (char*)&label, sizeof(FLBL1), (unsigned int*)&readsize);
	    if(status == MTEOF) {
	      mtspacef(vcb->fd, -1);
	      vcb->volloc = LEOV;
	      return;
	    }
	}
    }
}



/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      reclose	- This function closes a tape after header writing has been
**		  aborted. The easy way to do this is to backspace one
**		  file mark, write two filemarks, backspace one filemark
**		  and indicate we are at LEOV.
**
**  FORMAL PARAMETERS:
**
**      volume	*vcb	- Pointer to volume control block.
**
**
**--
*/
static void reclose (volume* vcb)
{
	
    mtspacef(vcb->fd, -1);
    mtweof(vcb->fd, 2);
    mtspacef(vcb->fd, 1);
    vcb->volloc = LEOV;
}


/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      nxtfileinfo - This function locates the next set of file headers on
**		      the tape (if there is a set) and decodes them into
**		      the volume control block.
**
**  FORMAL PARAMETERS:
**
**      volume	*vcb	- Pointer to the volume control block.
**	FLBL1   *hdr1	- POinter to buffer for "HDR1" record.
**	FLBL2   *hdr2	- POinter to buffer for "HDR2 record.
**
**  FUNCTION VALUE:
**
**      MTSUCCESS   - Found next file.
**	MTLEOV	    - End of volume before headers.
**	other	    - From various tape operations.
**
**
**--
*/
static int nxtfileinfo (volume* vcb, FLBL1* hdr1, FLBL2* hdr2)
{
    int status, 
	bytesread,
        first;

	/* The strategy is to skip files until we come across an	    */
	/* HDR1 record.  If after skipping files we hit a 		    */
	/* a tape mark, then we're at the end of volume.  In this we	    */
	/* rely on close to put us after the tape mark following	    */
	/* the EOF trailer labels. */
     
    first = 1;
    while(1)
    {
	status = mtread(vcb->fd,	/* Read the trial label */
			(char*)hdr1, sizeof(FLBL1),	/* If it's a tape mark  */
			(unsigned int *)&bytesread);		/* then we don't want to */
	if ((status == MTEOF)  && (!first))    	/* look any further, we  */
	{					/* Just back up in front */
	    mtspacef(vcb->fd, -2); 	       /* Of both (it's the last one */
	    return MTLEOV;			/* And return MLEOV    */
	}
	if((status == MTEOF) && first)		/* Could be sitting at */
	{					/* mark before data   */
	   status = mtread(vcb->fd,		/* So try read...     */
			   (char*)hdr1, sizeof(FLBL1),
			   (unsigned int*)&bytesread);
           if(status == MTEOF)			/* 2 EOF's in a row... */
	   {
	     mtspacef(vcb->fd, -2);		/* Backup over both    */
	     return MTLEOV;			/* Return end of volume */
           }
	}
        first = 0;			/* Leading EOF not possible till eov. */
	if ( (status != MTSUCCESS) &&
	     (status != MTOVERRUN))
	{
	    return status;
	}
	if ((bytesread == sizeof(FLBL1)) &&
	    (strncmp ("HDR1", hdr1->hdrid, IDSIZE) == 0)
            && (status == MTSUCCESS) )
	{					/* It's an EOF1 label. */
	    status = mtread(vcb->fd,
			    (char*)hdr2, sizeof(FLBL2),
			   (unsigned int*)&bytesread);
	    if ((status != MTSUCCESS)	||
		(bytesread != sizeof(FLBL2)) ||
		(strncmp ("HDR2", hdr2->hdrid, IDSIZE != 0)))
	    {
		return MTINVLBL;
	    }
	    mtspacef(vcb->fd, 1);	/* Position at data. */
	    return MTSUCCESS;
	}
	mtspacef(vcb->fd, 1);	/* Skip to next HDR1 */
    }
}


/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      volinit	- This function initializes a tape volume.  No attempts are made
**		    at this level to check if the volume was previously 
**		    initialized, or if the accessibility fields might prohibit
**		    initialization.  If application code wishes to do this, 
**		    it should mount the volume and inspect the volume header
**		    fields that are made available to the user.
**
**  FORMAL PARAMETERS:
**
**      int		handle	- fd open on tape device.
**	char		*label  - Name of volume (must be < 6 chars long).
**
**  FUNCTION VALUE:
**
**      Status of initialization.  Sould be MTSUCCESS.
**
**
**--
*/
int volinit (int handle, char* label)
{
    VOL1 header;
    int mtstat;

	/* First we need to validate everything. */
     
    if (strlen(label) > VOLLABEL)	    /* Label length */
    {
	return MTINVLBL;
    }
    if (!allachars(label))
    {
	return MTINVLBL;
    }
	/* Now that the label is valid, we can fill in the fields in it. */
     
    memset (&header, FILLCHAR, sizeof (VOL1));    /* Blank clear the header. */
    encstring(header.volid, "VOL1", IDSIZE); /* Set name of header. */
    encstring(header.volname, label, VOLLABEL); /* Set volume name. */
    encstring(header.volowner, STDOWNER, OWNERSIZE); /* Set standard owner. */
    encstring(header.implementor, STDIMP, IMPSIZE); /* Set standard implementor. */
    encstring(&(header.version), "4",1);	    /* Set ANSI rev level. */
     
	/* Now that the header is constructed, we can attempt to init. the */
	/* Volume.  This consist of loading the tape, writing the volume */
	/* label and writing a couple of end file marks.  After the tape is */
	/* Initialized, it is rewound to the load point. */
	 
    mtload(handle);		/* Load the drive. */
     
    mtstat = mtwrite(handle, (char*)&header,	/* Write the VOL1 label. */
		     sizeof(VOL1));
    if(mtstat != MTSUCCESS) return mtstat;

    mtstat = mtweof(handle,  2);		/* Mark tape empty. */
    if(mtstat != MTSUCCESS) return mtstat;
     
    mtstat = mtrewind(handle);		/* Return to load point. */
    return mtstat;
}


/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      volmount    - This function mounts a volume.  Mounted volumes may
**		    be read or written with file structure.
**
**  FORMAL PARAMETERS:
**
**      int		handle -  fd open on the drive
**	int		unit	- Unit number of device to mount.
**	volume_item	*itemlist- Pointer to the item list which describes
**				   the mount options.
**  FUNCTION VALUE:
**
**	Pointer to a volume control block if the mount succeeded or 0.
**	if the mount fails.  If the mount failed for any reason, then
**	errno holds the reason.
**--
*/
volume *volmount (int handle,  volume_item* itemlist)
{
    char *reqlbl,	/* Pointer to requested label if VOL_REQLBL */
	 *label,	/* Pointer to actual label buffer if VOL_LABEL */
	 *access,	/* Pointer to access field buffer if VOL_ACCESS */
	 *owner;	/* Pointer to owner field if VOL_OWNER */

    int *protect;	/* Pointer to protection mask if VOL_PROTECT */
    volume *vol;	/* Pointer to volume control block. */
    int status;
    VOL1 vollabel;		/* Volume 1 label buffer. */
    int actual;

    /* First we process the item list: */
     
    getitem(VOL_REQLBL,  itemlist, &reqlbl,  VOL_ENDLIST);
    getitem(VOL_PROTECT, itemlist, (char**)&protect, VOL_ENDLIST);
    getitem(VOL_LABEL,   itemlist, &label,   VOL_ENDLIST);
    getitem(VOL_ACCESS,  itemlist, &access,  VOL_ENDLIST);
    getitem(VOL_OWNER,   itemlist, &owner,   VOL_ENDLIST);
     
    /* Next, allocate and initialize the VCB. */
     
    if ((vol = initvcb(handle, handle)) == (volume *)0)
    {
	errno = MTINSFMEM;
	return 0;
    }
     
     /* Load the volume and read the volume label. */

    mtload(handle);
     
	/* The volume is considered ANSII if the first record is the */
	/* correct size and has the text "VOL1" in the volid position. */

    status = mtread(handle, (char*)&vollabel, sizeof(VOL1), (unsigned int*)&actual);
    if ((status != MTSUCCESS) ||
	(actual != sizeof(VOL1)))
    {
	errno = MTNOTANSI;
	free ((void*)vol);
	return 0;
    }
    if (strncmp ("VOL1", vollabel.volid, IDSIZE) != 0)
    {
	errno = MTNOTANSI;
	free ((void*)vol);
	return 0;
    }
     
    /* We need to make sure, that if the user passed us a requested label */
    /* that it is the same as the label we just read. */
     
    if (reqlbl)
    {
        int i;
        char labelupc[VOLLABEL+1];
	memset(labelupc, 0, sizeof(labelupc));
	strncpy(labelupc, reqlbl, VOLLABEL);
	for(i = 0; i < strlen(labelupc); i++) 
	  labelupc[i] = toupper(labelupc[i]);
	if (strncmp (labelupc, vollabel.volname, VOLLABEL) != 0)
	{
	    errno = MTWRONGVOL;
	    free (vol);
	    mtunload(handle);
	    return 0;
	}
    }
    /* If the caller passed in requested volume protections, then we	    */
    /* must apply them to override the default protections that the 	    */
    /* volume control block initializer set up for us. */
     
    if (protect)
    {
	switch (*protect)
	{
	    case READABLE:
		vol->access.readable = 1;
		vol->access.writeable = 0;
		break;
	    case WRITEABLE:
		vol->access.readable = 0;
		vol->access.writeable = 1;

	/* Since the volume is writeable, we			    */
	/* start positioning to the LEOV for write appends. */

		seekleov(vol);
		break;
	    default:
		free (vol);
		errno = MTBADPROT;
		return 0;
	}
    }
     
	/* If user passed a buffer for the actual volume label, then	    */
	/* copy the volume lable to his buffer, blanks and all. */

    decstring(vol->volname, vollabel.volname, VOLLABEL);
    if (label)
    {
	strncpy (label, vol->volname, VOLLABEL);
	label[VOLLABEL] = (char)0;
    }
     
    /* If the user wants to see the volume access field, then we copy it */
    /* to their character buffer. */
     
    if (access)
    {
	*access = vollabel.volaccess;
    }

    /* If the user wants to see the owner field, then we copy that too. */
     
    if (owner)
    {
	strncpy (owner, vollabel.volowner, OWNERSIZE);
	owner[OWNERSIZE] = 0;
    }

    /* At this time, the only thing we need to do is 	    */
    /* mark the unit mounted. */
     
    /* From here on in we can't fail. */
     
    vol->access.mounted = 1;
    vol->access.fileopen= 0;
    return vol;
}


/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      voldmount   - Dismount a volume from a tape drive.  The volume
**		      is described by a volume control block.  If a file
**		      is open on the volume we refuse to allow dismount.
**
**  FORMAL PARAMETERS:
**
**      volume	*vcb	- Pointer to a block which describes the volume.
**			  this storage was allocated by volmount and will
**			  be deallocated if the volume is successfully
**			  dismounted.
**
**
**  FUNCTION VALUE:
**
**      MTSUCCESS   - Normal completion.
**	MTBADHANDLE - VCB not valid.
**	MTNOTMOUNTED- Drive doesn't have a volume mounted.
**	MTFILEOPEN  - Volume has open file.
**	(others)    - from mtunlock and mtunload.
**
**--
*/
int voldmount (volume* vcb)
{
    int status;

    /* Make sure there's a chance he's handed us a volume control block. */

    if (vcb->magic != VOLUME_MAGIC)
    {
	return MTBADHANDLE;
    }
     
    /* If the volume is not mounted, cannot dismount. */
     
    if (!vcb->access.mounted)
    {
	return MTNOTMOUNTED;
    }
     
    /* If the volume has open files cannot dismount. */
     
    if (vcb->access.fileopen)
    {
	return MTFILEOPEN;
    }
     
    /* Now the only thing that can go wrong are hardware errors. */
     
    vcb->access.readable = 0;
    vcb->access.writeable= 0;
    vcb->access.mounted  = 0;
    vcb->access.fileopen = 0;

     
     /* Unload tape. */

    mtunload(vcb->fd);
     
    /* Before releasing the vcb we make darn sure that reusing it will fail. */
     
    vcb->magic = 0;
    vcb->fd   = -1;
    vcb->unit  = 0;
    free ((void*)vcb);

    return MTSUCCESS;
}


/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      volcreate   - Create a file on a mounted volume.  The logical end of
**		      volume is sought and the file headers written.
**		      The tapemark following the volume headers are also
**		      written and the tape is left positioned after that
**		      mark.  The volume state will be LEOV, and file open.
**		      file state will be BOF.
**
**  FORMAL PARAMETERS:
**
**      volume	*vcb	- Pointer to the volume control block.
**	file_item *items- Pointer to list of items describing how to do the
**			  open.
**
**
**  FUNCTION VALUE:
**
**      Overall status.  NOTE at this implementation, it's possible for
**	the system to crash on some tape I/O errors.  Later implementations
**	will only return bad status codes on these tape failures.
**
**
**--
*/
int volcreate (volume* vcb, file_item* items)
{
    char *userfilename;
    int *anint, 
	status;
    time_t ticks;
    tm_t lcltime;

    if (vcb->magic != VOLUME_MAGIC)
    {					/* Invalid volume control block. */
	return MTBADHANDLE; 
    }
    /* Validate the volume state.  We must be mounted, writable, and with */
    /* no file open.  If those conditions are met then we can seek for the */
    /* end of the volume. */
     
    if (! vcb->access.mounted)
    {
	return MTNOTMOUNTED;;
    }
    if (!vcb->access.writeable)
    {
	return MTPROTECTED;
    }
    if (vcb->access.fileopen)
    {
	return MTFILEOPEN;
    }
    seekleov(vcb);
    if (vcb->volloc != LEOV)	    /* If we couldn't get to EOV then */
    {				    /* we're off the end of the tape. */
	return MTEOMED;
    }
    /* The next thing we do is require a filename in the item list.	    */
    /* The filename is mapped to a valid ANSI filename (all 'a' characters  */
    /* and at most FILEIDENT characters. The result is stuck in the	*/
    /* VCB file identification section. */
     
    if ( !getitem(FILE_NAMEREQ, (volume_item*)items, &userfilename, FILE_ENDLIST) )
    {
	return MTFNAMEREQ;
    }
    nametoansi(vcb->filename, userfilename);
    vcb->blockswritten = 0;		/* Init block counter. */
    vcb->fileloc     = HEADERS;	/* In file headers. */
     
    /* Now we fill in the rest of the file information from either the    */
    /* defaults or the user's item list.  These items include:	    */
    /* creation	- Current date, no item list entry.		    */
    /* offlen	- 0 is default, from FILE_REQPREFIX otherwise.	    */
    /* reclen	- offlen + 8192 default, offlen + FILE_REQRECLEN otherwise */
    
    ticks = time(0); 
    memcpy (&vcb->creation,localtime(&ticks),sizeof(tm_t));
    if (getitem(FILE_REQPREFIX, (volume_item*)items, (char**)&anint, FILE_ENDLIST))
    {
	vcb->offlen = *anint;
    }
    else
    {
	vcb->offlen = 0;
    }
    if (getitem(FILE_REQRECLEN, (volume_item*)items, (char**)&anint, FILE_ENDLIST))
    {
	vcb->reclen = *anint;
    }
    else
    {
	vcb->reclen = DEFAULTRECSIZE;
    }
    /* At this point we are ready to try to write the headers.	    */
    /* If we ever fail to write the headers, we close the tape	    */
    /* using reclose()						    */
    /* All the information needed to write the labels is currently   */
    /* in the VCB with the exception of the file number, and the   */
    /* header title.  This is because we can use the same code	    */
    /* to write HDR's that we can to write EOF's */
     
    vcb->blockswritten = 0;
    status = lbl1write(vcb,		/* Most stuff here. */
		       "HDR1",		/* Label to write. */
		       vcb->filenumber+1);	/* Sequence number of file. */
    if (status != MTSUCCESS)
    {
	reclose(vcb);			/* If failed, reclose tape. */
	if ((status == MTLEOT))
	{
	    vcb->volloc = LEOT;

	}
	return status;
    }

    status = lbl2write(vcb, "HDR2");	/* Do the write of label 2. */
    if (status != MTSUCCESS)
    {
	reclose(vcb);			/* If failed, reclose tape. */
	if ((status == MTLEOT))
	{
	    vcb->volloc = LEOT;

	}
	return status;
    }
    /* At this point in time, the only obstacle to successful completion    */
    /* is to write the tape mark which separates headers from data. */
     
    status = mtweof(vcb->fd, 1);
    if (status != MTSUCCESS)
    {
	reclose(vcb);			/* If failed, reclose tape. */
	if ((status == MTLEOT))
	{
	    vcb->volloc = LEOT;

	}
	return status;
    }
    /* The rest is book keeping. */
     
    vcb->filenumber++;
    vcb->fileloc = BOF;
    vcb->access.fileopen = 1;
    vcb->access.readable = 0;	    /* From now on it's a writeonly tape. */

    /* Satisfy any item list entries which request final actual file 	    */
    /* information from the user. */

    if (getitem(FILE_NAME, (volume_item*)items, &userfilename, FILE_ENDLIST))
    {					    /* Wanted actual filename. */
	strcpy (userfilename, vcb->filename);
    }
    if (getitem(FILE_CREDATE, (volume_item*)items, (char**)&lcltime, FILE_ENDLIST))
    {					/* Wanted actual creation date. */

	memcpy (&lcltime,&(vcb->creation),sizeof(tm_t));
    }
    if (getitem(FILE_EXPDATE, (volume_item*)items, (char**)&lcltime, FILE_ENDLIST))
    {					/* Wanted actual expiration date. */

	memset (&lcltime,0, sizeof(tm_t));
    }
    if (getitem(FILE_ACCESS, (volume_item*)items, (char**)&anint, FILE_ENDLIST))
    {
	*((char *)anint) = ' ';		/* No accessibility field written. */
    }
    if (getitem(FILE_RECLEN, (volume_item*)items, (char**)&anint, FILE_ENDLIST))
    {					/* Requested actual record length. */
	*anint = vcb->reclen;
    }
    if (getitem(FILE_BLOCK, (volume_item*)items, (char**)&anint, FILE_ENDLIST))
    {					/* Requested actual block size. */
	*anint = vcb->reclen + vcb->offlen;
    }
    if (getitem(FILE_PREFIX, (volume_item*)items, (char**)&anint, FILE_ENDLIST))
    {					/* Requested actual offset len. */
	*anint = vcb->offlen;
    }

    return MTSUCCESS;
}


/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      volclose    - This function closes a file which is open on the volume.
**		     The file may be open for read or write.
**
**  FORMAL PARAMETERS:
**
**      volume	*vcb	- Pointer to the volume descriptor.
**
**  FUNCTION VALUE:
**
**	Status of the operations.
**--
*/
int volclose (volume* vcb)
{
    int status, 
	actualsize;
    FLBL1 label;

	/* The first thing we do is make sure the operation is even legal. */
	/* This includes the requirments that the vcb is valid.  	    */
	/* That the tape is mounted, and there is a file open. */

    if (vcb->magic != VOLUME_MAGIC)
    {
	return MTBADHANDLE;
    }
    if (!vcb->access.mounted)
    {
	return MTNOTMOUNTED;
    }
    if (!vcb->access.fileopen)
    {
	return MTFILECLOSE;
    }
    /* Next we take variable action depending on whether the file is open   */
    /* for write or read only. */
     
    if (vcb->access.writeable)
    {					/* File was volcreate()'ed. */
	status = writetrailers(vcb);	/* Write file trailer labesl,    */
					/* and tape marks. */
	vcb->access.fileopen = 0;	/* Markthe file closed. */
	vcb->volloc	     = LEOV;	/* We're at the logical EOV unless... */
	if (status == MTLEOT)		/* We're at logical end of tape or... */
	{
	    vcb->volloc	    = LEOT;
	}
	if (status == MTEOMED)		/* Pysical end of tape. */
	{
	    vcb->volloc = PEOT;
	}
    }
    else				/* File was volopen()'ed. */
    {
	vcb->access.fileopen = 0;	/* mark closed. */
	vcb->filenumber++;		/* On to next file. */
	vcb->volloc = MTMIDDLE;		/* We're in volume middle */
        status = MTSUCCESS;
    }
    return status;
}


/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      volmksafe   - Make a volume safe, but keep it open.  This is a special
**		      extension supplied for run pause.  Action differs
**		      from volclose() in only the following ways:
**			1. Tape is left positioned in front of the first
**			   EOF before the trailer lables, rather than
**			   before the second.
**			2. The file remains open.
**
**  FORMAL PARAMETERS:
**
**      volume *vcb - Pointer to volume control block.
**
**  FUNCTION VALUE:
**
**      Status of the attempt.
**
**
**--
*/
int volmksafe (volume* vcb)
{
    int status;

    status = volclose(vcb);	    /* Start by closing file. */
     
     if(status != MTEOMED)
	mtspacef(vcb->fd,	    /* Position in front of EOF's. */
	         -2);
     
    vcb->access.fileopen = 1;		/* The rest is book keeping. */
    vcb->filenumber--;
    vcb->volloc	= MTMIDDLE;
    vcb->fileloc = FILEMIDDLE;

    return status;
}



/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      volwrite    - This function writes data to a volcreate()'d file.
**
**  FORMAL PARAMETERS:
**
**      volume	*vcb	- Pointer to the volume control block.
**	char	*data   - Pointer to the data to write.
**	int	count	- Number of bytes to write.
**
**
**  FUNCTION VALUE:
**
**      status of the attempt.
**
**
**--
*/
int volwrite (volume* vcb, char* data, int count)
{
    int status;

    /* Before we believe anything we need to see that the vcb is good. */

    if (vcb->magic != VOLUME_MAGIC)
    {
	return MTBADHANDLE;
    }

    /* Before we can write we must have a mounted volume, file open	    */
    /* and writable setting in the vcb: */
     
    if (!vcb->access.mounted)
    {
	return MTNOTMOUNTED;
    }
    if (!vcb->access.fileopen)
    {
	return MTFILECLOSE;
    }
    if (!vcb->access.writeable)
    {
	return MTPROTECTED;
    }
     
    /* The other, less obvious problem is that the block must have the	    */
    /* same size as given in the VCB, else other implementations will	    */
    /* not like to read us. */
     
    if (count != (vcb->reclen+vcb->offlen))
    {
	return MTBADLENGTH;
    }
    /* At this point, the only thing that stands between us and victory	    */
    /* is getting the record to tape. */
     
    status = mtwrite(vcb->fd, data, count);
    vcb->blockswritten++;
    vcb->fileloc = FILEMIDDLE;

    /* Figuring out the final status requires figuring out the actual tape */
    /* position since he could have sailed past the leot. */

    if (status == MTSUCCESS)
    {
	if (vcb->volloc == LEOT)	    /* Make sure we're not past eof. */
	{
	    return MTLEOT;
	}
	else
	{
	    return MTSUCCESS;
	}
    }
    if (status == MTLEOT)		    /* If we just hit LEOT, remember. */
    {
	vcb->volloc = LEOT;
	return MTLEOT;
    }
    if (status == MTEOMED)		    /* Remember if we run out of */
    {					    /* Physical tape. */
	vcb->volloc = PEOT;
	return MTEOMED;
    }
    return status;
}


/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      volopen	- Function which opens a file for read access on an ANSI
**		  volume.  The function may either open the next file
**		  on tape (if FILE_NAMEREQ is not supplied), or a requested
**		  file (if it exists). The file is searched for forward
**		  from the current position. If not found, then the tape
**		  is rewound.  Thus if it is known that the file might be prior
**		  to the current tape position, the user should either:
**		    1. Dismount/mount/search
**		    2. Search twice if the first search fails.
**
**  FORMAL PARAMETERS:
**
**      volume	    *vcb	- Pointer to volume control block.
**	file_item   *itemlist	- Pointer to item list describing the open.
**
**  FUNCTION VALUE:
**
**      status of the attempt.  If the file is not found, this will be
**	MTNOTFOUND.
**
**--
*/
int volopen (volume* vcb, file_item* itemlist)
{
    FLBL1   header1;
    FLBL2   header2;
    char    mappedname[FILEIDENT+1];	/* Mapped requested filename */
    char    actualname[FILEIDENT+1]; /* Null terminated actual filename. */
    char *requestedname, 
	 *string;
    int *value,
	status;

    /* First we determine if the operation is valid. */
     
    if (vcb->magic != VOLUME_MAGIC)
    {
	return MTBADHANDLE;
    }
    if (! vcb->access.mounted)
    {
	return MTNOTMOUNTED;
    }
    if (vcb->access.fileopen)
    {
	return MTFILEOPEN;
    }
    if (!vcb->access.readable)
    {
	return MTPROTECTED;
    }
    /* Then we search for the file on tape. */
     
    requestedname = (char *)0;
    getitem(FILE_NAMEREQ, (volume_item*)itemlist,		/* Get desired filename. */
	    &requestedname, FILE_ENDLIST);
    if(requestedname != NULL) {			/* Convert to A chars for */
       memset(mappedname, ' ', sizeof(mappedname)); /* the match.	*/
       mappedname[FILEIDENT] = '\0';		/* Null termintate	*/
       strncpy(mappedname, requestedname, FILEIDENT);
       {
	 int i;
	 for(i = strlen(mappedname); i <= FILEIDENT; i++)
	   mappedname[i] = ' ';
       }
       requestedname = nametoansi(mappedname, mappedname);

    }
    if (vcb->volloc == LEOV)			/* If we're sitting at the */
    {						/* End of written tape. */
	mtrewind(vcb->fd);		/* Then rewind and indicate */
	mtspacer(vcb->fd, 1);	/* Not found to caller. */
	vcb->volloc = BOT;
	return MTNOTFOUND;
    }
    while ((status = nxtfileinfo(vcb, &header1, &header2)) == MTSUCCESS)
    {
	if (requestedname == 0)
	{
	    break;			/* If no name, accept any match. */
	}
        strncpy(actualname, header1.filename, FILEIDENT);
	actualname[FILEIDENT] = 0;
	if (strncmp (requestedname, actualname, FILEIDENT) == 0)
	{
	    break;			/* Name matches. */
	}
    }

    if (status != MTSUCCESS)
    {
	if (status == MTLEOV)		    /* Not found. */
	{
	    mtrewind(vcb->fd);
	    mtspacer(vcb->fd, 1);
	    return MTNOTFOUND;
	}
	return status;
    }
    /* At this time, the file is positioned following the */
    /* tape mark after it's HDR records. Fill in the VCB from the */
    /* Header labels, and what we know. */
     
    vcb->volloc = MTMIDDLE;
    vcb->filenumber =decnum(header1.seqnum, NUMSIZE);
    decstring(vcb->filename, header1.filename, FILEIDENT);
    vcb->blockswritten = 0;
    vcb->access.readable = 1;
    vcb->access.writeable = 0;
    vcb->access.fileopen = 1;
    vcb->fileloc = BOF;
    decdate(&(vcb->creation), header1.credate);
    vcb->reclen = decnum(header2.recordlen, BLKSIZE);
    vcb->offlen = decnum(header2.offlen, OFFSIZE);
     
    /* Now satisfy the callers requests for file information that are in */
    /* the item list. */
     
    if (getitem(FILE_NAME, (volume_item*)itemlist, &string, FILE_ENDLIST))
    {
	strcpy (string, vcb->filename);
    }
    if (getitem(FILE_CREDATE, (volume_item*)itemlist, (char**)&value, FILE_ENDLIST))
    {
	memcpy (value,&(vcb->creation),sizeof(tm_t));
    }
    if (getitem(FILE_EXPDATE, (volume_item*)itemlist, (char**)&value, FILE_ENDLIST))
    {
	decdate((tm_t*)value, header1.expdate);
    }
    if (getitem(FILE_ACCESS, (volume_item*)itemlist, (char**)&string, FILE_ENDLIST))
    {
	*string = header1.access;
    }
    if (getitem(FILE_RECLEN, (volume_item*)itemlist, (char**)&value, FILE_ENDLIST))
    {
	*value = vcb->reclen;
    }
    if (getitem(FILE_BLOCK, (volume_item*)itemlist, (char**)&value, FILE_ENDLIST))
    {
	*value = vcb->reclen + vcb->offlen;
    }
    if (getitem(FILE_PREFIX, (volume_item*)itemlist, (char**)&value, FILE_ENDLIST))
    {
	*value = vcb->offlen;
    }
    return MTSUCCESS;
}


/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      volread	- This function reads a block from a tape volume.
**
**  FORMAL PARAMETERS:
**
**      volume	    *vcb    - Pointer to the volume control block.
**	char	    *data   - Pointer to block of data to read
**	int	    count   - Number of bytes to read
**	int	    *actual - Number of bytes actually read.
**
**
**  FUNCTION VALUE:
**
**      Status of the read.
**
**--
*/
int volread (volume* vcb, char* data, int count, int* actual)
{
    int status;

    /* First validate the operation: */
     
    if (vcb->magic != VOLUME_MAGIC)
    {
	return MTBADHANDLE;
    }
    if (!vcb->access.mounted)
    {
	return MTNOTMOUNTED;
    }
    if (!vcb->access.fileopen)
    {
	return MTFILECLOSE;
    }
    if (!vcb->access.readable)
    {
	return MTPROTECTED;
    }
    if (count != (vcb->reclen + vcb->offlen))
    {
	return MTBADLENGTH;
    }
    if (vcb->fileloc == EOF)
    {
	return MTEOF;
    }
    status = mtread(vcb->fd,
		    data, count, (unsigned int*)actual);
    if (status == MTEOF)
    {
	mtspacef(vcb->fd, -1);
	vcb->fileloc = EOF;
	*actual = 0;
	return MTEOF;
    }
   vcb->blockswritten++;
#ifdef SPARC			/* Try to avoid the 2gbyte limit... */
    if(vcb->blockswritten > 1000) { /* Don't lseek all the time. */
      lseek(vcb->fd, 0, SEEK_SET);
      vcb->blockswritten = 0;
    }
#endif
    return status;
}
