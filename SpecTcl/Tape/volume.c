/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      isachar	- This function returns true if the character passed
**		    is an 'a' character.
**
**  FORMAL PARAMETERS:
**
**      char	c   - Character to test.
**
**  FUNCTION VALUE:
**
**      1   - c is an 'a' character
**	0   - c is not an 'a' character.
**
**
**--
*/
static int isachar (char c)
{

	if (islower(c))
	{
	    return 0;
	}
	if (iscntrl(c))
	{
	    return 0;
	}
	if ((c == '#')   ||
	    (c == '$')   ||
	    (c == '@')   ||
	    (c == ']')   ||
	    (c == '\\')  ||
	    (c == '[')   ||
	    (c == '^')   ||
	    (c == '{')   ||
	    (c == '}')   ||
	    (c == '!')   ||
	    (c == '~')
				)
	{
	    return 0;
	}
    return 1;
}


/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      allachars   - Function to determine if a field is only 'a' characters.
**
**  FORMAL PARAMETERS:
**
**      char	*src	- String to check.
**
**
**  FUNCTION VALUE:
**
**      1   - src is all a chars.
**	0   - src is not all a chars.
**
**
**--
*/
static int allachars (char* src)
{
    register char c;

    while ((c = *src++))
    {

	if (!isachar(c))
	{
	    return 0;
	}
    }
    return 1;
}



/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      nametoansi  - Converts a filename to a valid ansi filename.
***		      this is done by truncating the filename as needed
**		      to FILEIDENT characters and mapping all non 'a'
**		      characters to underscores.  Note that lower case
**			characters are mapped to upper case.
**
**  FORMAL PARAMETERS:
**
**      char *out   -	Output string 
**	char *in    -	Input string.
**
**  FUNCTION VALUE:
**
**      Pointer to out to allow use in function arguments.
**
**
**--
*/
static char *nametoansi (char* out, char* in)
{
    char *dest;
    char c;
    int n;

	/* Compute the size of the string and truncate if needed. */

    dest = out;
    n = strlen (in);
    if (n > FILEIDENT)
    {
	n= FILEIDENT;
    }
    /* Now do the mapping in the copy: */
     
    while (n-- > 0)
    {
	c = *in++;
	if(islower(c)) c = toupper(c);	/* Translate lower to upper */
	if (isachar(c))
	{
	    *dest++ = c;
	}
	else
	{
	    *dest++ = '_';
	}
    }
/*    *dest = '\0'; */	    /* Tack on zero terminator. */
    return out;
}


/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      encnum	- Encode a number into an ANSI field.  The number is
**		    encoded so that it is zero filled right justified.
**
**  FORMAL PARAMETERS:
**
**      char	*fld	    - Destniation field.
**	int	value	    - Number to encode.
**	int	length	    - Number of characters in field.
**
**
**--
*/
static void encnum (char* fld, int value, int length)
{
    char *d;
    int digit;

    d = &fld[length-1];		/* We'll fill backwards. */
    while (length-- > 0)	/* Only quitting at length = 0 causes */
    {				/* Automatic left zero filling. */
	digit = value % 10;
	value /= 10;		/* Extract low order digit. */
	*d-- = digits[digit];
    }
}


/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      encdate	- Encodes a date in an ANSI date field.  ANSI date fields are
**		  rather strnage:
**		    first character is the first two digits of the year:
**			' ' -> 19
**			'0' -> 20
**		    next two characters are the least two significant digits
**		    of the year:
**			("00" - "99').
**		    next three characters are the day within the year.
**			("001"-"366")
**
**  FORMAL PARAMETERS:
**
**      char	*field	- Pointer to the ANSI date field.
**	tm_t *date	- Pointer to time structure.
**	
**
**  IMPLICIT INPUTS:
**
**      month_lengths	- Array of number of days in a month.
**
**
**--
*/
static void encdate (char* field, tm_t* date)
{
    int yrlow,			/* Low order 2 digits of year. */
	i, 
	days;

    /* First we do the year part of the date.			    */
    /* Assumption:  Years are either 19xx or 20xx  This is an ANSI    */
    /* standard assumption too, so it's not too bad. */
     
    if (date->tm_year > 100)
    {
	field[0] = '0';		    /* 0 Signifies 20xx years. */
	yrlow   = date->tm_year - 100;
    }
    else
    {
	field[0] = ' ';		     /* Blank specifies 19xx years. */
	yrlow	= date->tm_year;
    }
    encnum(&(field[1]), (int)yrlow, 2);	/* Encode rest of year. */
     
    /* Now we compute the number of days into the year for the date given. */
     
    days = date->tm_mday;
    for (i = 1;  i <  date->tm_mon;  i++)
    {
	days += month_lengths[i];
    }
	/* Deal with leap years if appropriate. */

    if ((date->tm_mon > FEBRUARY) && leapyear(date->tm_year))
    {
	days++;
    }
    encnum(&(field[3]), (int)days, 3);	/* Encode the day. */
}


/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      encstring    - This function copies a string into a label field
**		      the string is truncated at the end of the label
**			and the zero EOS character is not copied.
**
**  FORMAL PARAMETERS:
**
**      char *dest	- Pointer to destination field.
**	char *src	- Pointer to source data.
**	int  length	- Length of output field.
**
**
**--
*/
static void encstring(char* dest, char* src, int length)
{

	/* Copy the string, stopping at end, or length overflow. */

    while ((length > 0) && (*src))
    {
	*dest++ = *src++;
	length--;
    }
	/* If there's space afterwards, then blankfill. */

    while (length-- > 0)
    {
	*dest++ = FILLCHAR;
    }
}


/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      decnum	- Decode number from an input field of fixed length.
**
**  FORMAL PARAMETERS:
**
**      char	*field	- Pointer to the field.
**	int	len	- Number of characters in field.
**
**  FUNCTION VALUE:
**
**      Decoded value of the number.
**
**
**--
*/
static int decnum (char* field, int len)
{
    int sum;

    sum = 0;
    for (;  len > 0;  len--)
    {
	sum *= 10;
	switch (*field++)
	{
	    case '9':		    /* We rely on the fall through to */
		sum++;		    /* subsequent cases to complete the */
	    case '8':		    /* job. */
		sum++;
	    case '7':
		sum++;
	    case '6':
		sum++;
	    case '5':
		sum++;
	    case '4':
		sum++;
	    case '3':
		sum++;
	    case '2':
		sum++;
	    case '1':
		sum++;
	    case '0':
		;
	}
    }
    return sum;
}


/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      decstring   - Decode a string from an ANSI label field.
**
**  FORMAL PARAMETERS:
**
**      char	*str	- Pointer to output string.
**	char	*field  - Pointer to input field.
**	int	len	- Number of chars in input field.
**
**  FUNCTION VALUE:
**
**      char * pointer to str. so this can be used in functions.
**
**
**--
*/
static char *decstring (char* str, char* field, int len)
{
    char *d;

    d = str;
    while ( len-- > 0 )
    {
	*d++ = *field++;
    }
    *d = '\0';

    return str;
}


/*
**++
**  FUNCTIONAL DESCRIPTION:
**
**      decdate	- This function converts a date to PSOS date format.
**		    The ANSI standard uses a rather ludicrous format for
**		    the date.  The date is coded:
**		      fyyddd
**		    Where:
**			f   - ' ' - indicates century is 1900
**			      '0' - indicates century is 2000
**			yy  - Is the last two digits of the year.
**			ddd - Is the day of the year.
**
**  FORMAL PARAMETERS:
**
**      tm_t    *date   - Pointer to output date structure.
**	char	    *field  - Pointer to ANSI date field.
**
**
**--
*/
static void decdate (tm_t* date, char* field)
{
    int daysinmonth, 
	day,
	month;

	/* First decode the year. */

    switch (*field++)
    {
	case ' ':
	    date->tm_year = 1900;
	    break;
	case '0':
	    date->tm_year = 2000;
	    break;
    }
    date->tm_year += decnum(field, 2);
    field += 2;
     
    /* Next decode the month/day from the day of the year. */     
    day = decnum(field,3);
    date->tm_mon = 1;
    for (month = 1;  month < 12;  month++)
    {
	daysinmonth = month_lengths[month];
	if ((month == FEBRUARY) && leapyear(date->tm_year))    /* Pesky feb. */
	{
	    daysinmonth++;				    /* on leapyrs */
	}
	if (day > daysinmonth)
	{
	    date->tm_mon++;			/* One more month in. */
	    day -= daysinmonth;			/* Decrease the day count. */
	}
	else				    /* Done with count down. */
	{
	    break;
	}
    }
    date->tm_mday = day;
}


