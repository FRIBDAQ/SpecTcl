// Function:
//    void Usage(CTCLResult& rResult)
// Operation type:
//    protected utility.
//
void
ChannelCommand::Usage(CTCLResult& rResult)
{
  rResult += "Usage:\n";
  rResult += "   channel -get spectrumname { indices }\n";
  rResult += "   channel -set spectrumname { indices } NewValue\n";

}
