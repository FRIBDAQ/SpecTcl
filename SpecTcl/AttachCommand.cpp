//  Function:
//    Switch_t ParseSwitch(char* pSwitch)
//  Operation Type:
//    Protected Utility.
//
CAttachCommand::Switch_t
CAttachCommand::ParseSwitch(char* pSwitch) {
  for(UInt_t i = 0; i < nSwitches; i++) {
    if(strcmp(SwitchTable[i].pName, pSwitch) == 0)
      return SwitchTable[i].Value;
  }
  return keNotSwitch;
}


