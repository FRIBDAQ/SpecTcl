/*!
  Construct a position calculator.  This does not do any
  binding of the tcl variables nor parameters as they are
  not assumed to be ready to be bound:
  - The Tcl variables are bound at the first OnBegin call.
  - The parameters are re-bound every OnBegin call.

  \param Result (const string& [in]): 
    Name of the position parameter produced.
  \param Param1 (const string& [in]):
    Name of the first parameter that is used in the position 
    computation.
  \param Param2 (const string& [in]):
     Name of the second parameter that is used in the position 
     computation.
  \param Scale1 (const string& [in]):
     Scale factor to calibrate parameter1.
  \param Offset1 (const string& [in]):
     Offset to calibrate parameter 1.
  \param Scale2 (const string& [in]):
     Scale to calibrate parameter2.
  \param Offset2 (const string& [in]):
     Offset to calibrate paramteter2.
  \param SumMin (const string& [in]):
     Name of paramter giving minimum valid sum signal.
  \param Slope  (const string& [in]):
     Name of the final slope parameter.
  \param Offset (const string& [in]):
     Name of the final offset parameter.

*/
CPosition::CPosition(const string& Position, 
		     const string& Param1, const string& Param2,
		     const string& Scale1, const string& Offset1,
		     const string& Scale2, const string& Offset2,
		     const string& SumMin,
		     const string& Slope, const string& Offset)  :
  m_Result(Position),
  m_Param1(Param1),
  m_Param2(Param2),
  m_pParam1(0),
  m_pParam2(0),
  m_Scale1Name(Scale1),
  m_Scale2Name(Scale2),
  m_Offset1Name(Offset1),
  m_Offset2Name(Offset2),
  m_SumMinName(SumMin),
  m_SlopeName(Slope),
  m_OffsetName(Offset),
  m_fScale1(1.0),
  m_fScale2(1.0),
  m_fOffset1(0.0),
  m_fOffset2(0.0),
  m_fSumMin(1.0),
  m_fSlope(1.0),
  m_fOffset(0.0),
  m_fBound(false)

  
{

}
/*!
  Copy constructor, uses the Copy utility.
*/
CPosition::CPosition(const CPosition& rhs) :
  CEventProcessor(rhs)
{
  Copy(rhs);
}
/*!
   Assignment... uses Copy utility.
*/
CPosition&
CPosition::operator=(const CPosition& rhs)
{
  if(this != &rhs) {
    CEventProcessor::operator=(rhs);
    Copy(rhs);
  }
  return *this;
}
/*!
  Equality comparison.  This is a shallow compare.
*/
int
CPosition::operator==(const CPosition& rhs) const
{
  return  (CEventProcessor::operator==(rhs)     &&
	   (m_Result     == rhs.m_Result)       &&
	   (m_Param1     == rhs.m_Param1)       &&
	   (m_Param2     == rhs.m_Param2)       &&
	   (m_pParam1    == rhs.m_pParam1)      &&
	   (m_pResult    == rhs.m_pResult)      &&
	   (m_Scale1Name == rhs.m_Scale1Name)   &&
	   (m_Scale2Name == rhs.m_Scale2Name)   &&
	   (m_Offset1Name== rhs.m_Offset1Name)  &&
	   (m_Offset2Name== rhs.m_Offset2Name)  &&
	   (m_SumMinName == rhs.m_SumMinName)   &&
	   (m_SlopeName  == rhs.m_SlopeName)    &&
	   (m_OffsetName == rhs.m_OffsetName)   &&
	   (m_fScale1    == rhs.m_fScale1)      &&
	   (m_fScale2    == rhs.m_fScale2)      &&
	   (m_fOffset1   == rhs.m_fOffset1)     &&
	   (m_fSumMin    == rhs.m_fSumMin)      &&
	   (m_fSlope     == rhs.m_fSlope)       &&
	   (m_fOffset    == rhs.m_fOffset)      &&
	   (m_fBound     == rhs.m_fBound));
}
/*!
  Called when the run begins.  If this is the first time,
  we bind the calibration variables to the member variables.
  Regardless, we map the parameter names to histogramer paramters.
  \param rAnalyzer (CAnalyzer& [in]):
     reference to our analyzer.
  \param rDecoder (CBufferDecoder& [in]):
     Reference to our buffer decodeer object.

  \return Bool_t
     - kfTrue  - if everything worked.
     - kfFalse - if there was a failure, this stops the analysis 
                  pipeline

*/
Bool_t
CPosition::OnBegin (CAnalyzer& rAnalyzer, 
		    CBufferDecoder& rDecoder)
{

  // If necessary, bind the variables:

  if(!m_fBound) {
    
    CTCLVariable scale1(gpInterpreter,  m_Scale1Name,  kfFALSE);
    CTCLVariable scale2(gpInterpreter,  m_Scale2Name,  kfFALSE);
    CTCLVariable offset1(gpInterpreter, m_Offset1Name, kfFALSE);
    CTCLVariable offset2(gpInterpreter, m_Offset2Name, kfFALSE);
    CTCLVariable minsum(gpInterpreter,  m_SumMinName,  kfFALSE);

    CTCLVariable slope(gpInterpreter,   m_SlopeName,   kfFALSE);
    CTCLVariable offset(gpInterpreter,  m_OffsetName,  kfFALSE);
    
    // Link the variables to their associated members.
    // Note that the value used for the Tcl variable initially
    // is that of the bound C/C++ variable.  Therefore we first
    // need to get the actual value of the Tcl variable, set it
    // in the variable we'll bind to it and then link the variable.
    //

    m_fScale1 = GetDoubleValue(scale1, m_fScale1);
    scale1.Link(&m_fScale1, TCL_LINK_DOUBLE);

    m_fScale2 = GetDoubleValue(scale2, m_fScale2);
    scale2.Link(&m_fScale2, TCL_LINK_DOUBLE);

    m_fOffset1 = GetDoubleValue(offset1, m_fOffset1);
    offset1.Link(&m_fOffset1, TCL_LINK_DOUBLE);

    m_fOffset2 = GetDoubleValue(offset2, m_fOffset2);
    offset2.Link(&m_fOffset2, TCL_LINK_DOUBLE);
    
    m_fSumMin = GetDoubleValue(minsum, m_fSumMin);
    minsum.Link(&m_fSumMin, TCL_LINK_DOUBLE);

    m_fSlope = GetDoubleValue(slope, m_fSlope);
    slope.Link(&m_fSlope,   TCL_LINK_DOUBLE);

    m_fOffset= GetDoubleValue(offset, m_fOffset);
    offset.Link(&m_fOffset, TCL_LINK_DOUBLE);

    m_fBound = true;		// Only need to bind first time.
  }
  
  // Rebind the parameter names:
  // Note that null paramters are possible and indicate a lack
  // of desire for these values.
  //

  CHistogrammer& Hist(*(CHistogrammer*)gpEventSink);
  m_pParam1 = Hist.FindParameter(m_Param1);
  m_pParam2 = Hist.FindParameter(m_Param2);
  m_pResult = Hist.FindParameter(m_Result);



  return kfTRUE;
}

/*!
   Calculate the position We depend on the following member data:
   - m_pResult - Pointer to the parameter describing where the
                 result goes.
   - m_pParam1 - First parameter in the position computation,
                 e.g. left.
   - m_pParam2 - Second parameter in the position computation,
                 e.g. right.
   - m_fScale1, m_fOffset1  - Calibrations for param1.
   - m_fScale2, m_fOffset2  - Calibrations for param2.
   - m_fSumMin - Minimum required sum for the parameters.
   - m_fSlope, m_fOffset - maps the range [-1.0,1.0] to 
               parameter channels.

   \note If any of the paramters is not defined, the computation
         is disabled.

   \param pEvent (const Address_t [in]):
     Pointer to the raw event (ignored).
   \param rEvent (CEvent& [in/out]):
     The event being built.  We take the specified two parameters
     as input and produce a single output parameter in this
     `array.'
   \param rAnalyzer (CAnalyzer& [in]):
     Reference to the analyzer (ignored).
   \param rDecoder (CBufferDecoder& [in]):
     Reference to the buffer decoder (ignored).

   \return Bool_t
      kfTRUE - It's not possible for this event processor to fail.
*/
Bool_t
CPosition::operator() (const Address_t pEvent, 
			       CEvent& rEvent, 
			       CAnalyzer& rAnalyzer, 
			       CBufferDecoder& rDecoder)
{
  // All parameters must map to real parameters:

  if(m_pParam1 && m_pParam2 && m_pResult) {
    
    // Map parameter numbers to rEvent indices:

    int p1 = m_pParam1->getNumber();
    int p2 = m_pParam2->getNumber();
    int r  = m_pResult->getNumber();

    // p1, p2 must be valid:

    if(rEvent[p1].isValid()  && rEvent[p2].isValid()) {
      double scaled1 = ((double)rEvent[p1] + drand48())*m_fScale1 +
	                                                m_fOffset1;
      double scaled2 = ((double)rEvent[p2] + drand48())*m_fScale2 +
	                                                m_fOffset2;
      double sum     = scaled1 + scaled2;
      if(sum > m_fSumMin) {
	rEvent[r] = ((scaled2 - scaled1)/sum + 1.0)*m_fSlope +
	  m_fOffset;
      }
    }
  }


  // always return true:

  return kfTRUE;
}
/*!
   Copy from another CPosition to *this.
*/
void
CPosition::Copy(const CPosition& rhs)
{
  m_Result     = rhs.m_Result;
  m_Param1     = rhs.m_Param1;
  m_Param2     = rhs.m_Param2;
  

  m_pParam1    = rhs.m_pParam1;
  m_pParam2    = rhs.m_pParam2;
  m_pResult    = rhs.m_pResult;

  m_Scale1Name = rhs.m_Scale1Name;
  m_Scale2Name = rhs.m_Scale2Name;
  m_Offset1Name= rhs.m_Offset1Name;
  m_Offset2Name= rhs.m_Offset2Name;

  m_SumMinName = rhs.m_SumMinName;
  m_SlopeName  = rhs.m_SlopeName;
  m_OffsetName = rhs.m_OffsetName;

  m_fScale1    = rhs.m_fScale1;
  m_fScale2    = rhs.m_fScale2;
  m_fOffset1   = rhs.m_fOffset1;
  m_fOffset2   = rhs.m_fOffset2;
  m_fSumMin    = rhs.m_fSumMin;

  m_fSlope     = rhs.m_fSlope;
  m_fOffset    = rhs.m_fOffset;
  
  m_fBound     = rhs.m_fBound;
  
}


/*!
    This function returns the value of a TCL variable
    converted to a double.  If an exception was thrown
    by the get or the convert (variable doesn't exist or
    has a non double expression), a default is supplied.
    \param var (CTCLVariable& [in]):
        Reference to the variable to convert.
    \param def (double [in]):
        Default value for the variable.
    \return double 
       Value of the variable or the default.
*/
double
CPosition::GetDoubleValue(CTCLVariable& var, double def)
{
  try {
    string value(var.Get());
    return var.getInterpreter()->ExprDouble(value);
  }
  catch (...) {
    return def;
  }
}
