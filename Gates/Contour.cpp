// Local data types:

class YCompare {
public:
  int operator()(const FPoint& p1, const FPoint& p2) {
    return (p1.Y() < p2.Y());
  }
};
class XCompare {
public:
  int operator()(const FPoint& p1, FPoint& p2) {
    return (p1.X() < p2.X());
  }
};




