// Local data types:

struct Edge {
  Float_t xnow;
  Float_t ylast;
  Float_t xadjust;
  Edge(Float_t x1 = 0.0, Float_t yl = 0.0, Float_t invslope = 0.0) :
    xnow(x1),
    ylast(yl),
    xadjust(invslope) {}
  
};


typedef vector<Edge> EdgeList, *pEdgeList;

struct EdgeTable {
  UInt_t nLines;
  UInt_t nLowline;
  vector<EdgeList> Edges;
};

