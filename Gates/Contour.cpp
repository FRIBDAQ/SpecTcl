//

class CXnowCompare {
public:
  int operator()(const Edge& p1, const Edge& p2) {
    return p1.xnow < p2.xnow;
  }
};
class YCompare {
public:
  int operator()(const CPoint& p1, const CPoint& p2) {
    return (p1.Y() < p2.Y());
  }
};
class XCompare {
public:
  int operator()(const CPoint& p1, CPoint& p2) {
    return (p1.X() < p2.X());
  }
};


